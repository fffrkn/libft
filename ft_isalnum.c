#include "libft.h"

int	ft_isalnum(int i)
{
	if (('a' <= i && 'z' >= i) || ('A' <= i && 'Z' >= i)
		|| ('0' <= i && '9' >= i))
	{
		return (1);
	}
	return (0);
}
