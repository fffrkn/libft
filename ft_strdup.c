#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	size_t	length;
	size_t	i;

	i = 0;
	length = ft_strlen(s1) + 1;
	dest = (char *)malloc(length * 1);
	if (dest == 0)
		return (0);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
